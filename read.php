    <p>Surah Fatiha is the opening chapter of the Quran. It comprises of seven verses.; the first three are in praise of Allah Subhanu Wataa’la, while the last three contains a request or a prayer on the part of a man, which Allah himself has taught. The verse in between the two sets has both the features, there is an aspect of praise and another of prayer.

        Saaiduna Abu Hurarirah Radiallahu Anhu reports from the Prophet of Allah Sallallahu Alahi Wasalam:
        
        “The salah (Surah al Fatiha) is equally divided between me and my servant. And my servant shall be given what he prays for. The Prophet of Allah Sallallahu Alahi Wasalam continued; “when the servant says; ‘Alhamdullilahi Rabil Alamin.’ Allah says; “My servant has paid his homage to me; “when he says ‘Ar Rahmanir Rahim’ Allah says; “My servant has praised Me.” when the servant says ‘Maliki yaw middin’
    Allah says; “My servant has proclaimed my greatness.” When the servant says; ‘Iyaknabudu wa iyakanastaeen’, Allah says “ This verse is common to me and my servant he shall be given what he prays for.” (Sahih Muslim)
            
            The surah begins with the words Al hamdulillah, signifying that all praise belongs to Allah. Whosoever praises anything anywhere in the world is praising all mighty Allah. The translation of the entire Surah (Chapter) is as follows</p>
            
